#include <unistd.h>

void	print_bits(unsigned char octet)
{
	int d;

	d = 128;
	while (d)
	{
		if (d <= octet)
		{
			write(1, "1", 1);
			octet = octet % d;
		}
		else
			write(1, "0", 1);
		d /= 2;
	}
}

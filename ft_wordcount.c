int		ft_wordcount(char const *s, char c)
{
	int		t;

	t = 0;
	if (!s)
		return (0);
	while (*s)
	{
		if (*s == c)
			s++;
		else
		{
			if (*(s + 1) == c || !(*(s + 1)))
				t++;
			s++;
		}
	}
	return (t);
}

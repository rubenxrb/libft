#include "core.h"

int		ft_isblank(int c)
{
	return ((c == ' ' || c == '\t' || c == '\n' || c == '\v' || c == '\f'
	|| c == '\r'));
}

int		ft_islower(int c)
{
	return (c >= 'a' && c <= 'z');
}

int		ft_isupper(int c)
{
	return (c >= 'A' && c <= 'Z');
}

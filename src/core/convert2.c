#include "core.h"

int		ft_toupper(int c)
{
	if (ft_isalpha(c))
		if (c >= 97 && c <= 122)
			c -= 32;
	return (c);
}

int		ft_tolower(int c)
{
	if (ft_isalpha(c))
		if (c >= 65 && c <= 90)
			c += 32;
	return (c);
}
